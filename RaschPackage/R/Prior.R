#' Finding prior for a given value of theta
#'
#' Finds height of normal curve with mean = 0, sd = 3 at theta
#'
#' @param theta A numeric object.
#'
#' @return An object of class Numeric that represents a prior for theta
#' @author Tyler Brandt
#' @note This function is for test takers
#' @examples
#' 
#' myTheta <- 3
#' Prior(myTheta)
#' @rdname Prior
#' @aliases Prior, Prior Numeric Method
#' @export
setGeneric(name = "Prior",
           def = function(theta){
             standardGeneric("Prior")
           })

setMethod(f = "Prior",
          signature = "numeric",
          definition = function(theta){
            return (dnorm(theta, mean = 0, sd = 3))
          })
